exports.images = function(breakpoints, window){
  let imageSize;
  let imagesSized = {};
  let imagesSrc = [];
  let imagesAll = {};
  let currentBreakpoint;

  let size = Object.keys(breakpoints);
  for(let i = 0; i < images.length; i++){
    let image = images[i];
    for(let s = size.length-1; s >= 0; s--){
      if(window >= breakpoints[size[s]]){
        currentBreakpoint = size[s];
        if(image.src[size[s]]){
          imagesSized[image.name] = image.src[size[s]];
          imagesSrc.push(image.src[size[s]]);
          s = 0;
        }
      }
    }
    imagesAll[image.name] = Object.assign({project: image.project},image.src);
  }
  return {
    sized: imagesSized,
    sources: imagesSrc,
    all: imagesAll,
    currentBreakpoint: currentBreakpoint
  }
}
